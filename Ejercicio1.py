# Ejercicio 1: Ingresar dos números enteros e imprimir la suma, resta y multiplicación de ambos.

#Ingreso los números
a = int(input("Por favor, ingrese el número 1: \n"))
b = int(input("Por favor, ingrese el número 2: \n"))

#Imprimo las operaciones solicitadas
print("La suma de los números ingresados es ", a + b, ".") 
print("La resta de los números ingresados es ", a - b, ".") 
print("La multiplicación de los números ingresados es ", a * b, ".") 